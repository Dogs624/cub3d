#include "libft.h"

void	ft_free_tab(char **tab)
{
	int	i;

	i = -1;
	while (tab[++i])
	{
		free(tab[i]);
		tab[i] = NULL;
	}
	if (tab)
	{
		free(tab);
		tab = NULL;
	}
}
